const createLevel = (id: number, size: number, pairsData: [number, number, number, number][]): Level => {
  return {
    id,
    size,
    pairs: pairsData.map((p, i) => ({
      color: COLORS[i % COLORS.length],
      start: { x: p[0], y: p[1] },
      end: { x: p[2], y: p[3] }
    })),
    solutions: {}
  };
};

